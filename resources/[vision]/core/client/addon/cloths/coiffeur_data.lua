function HairCutAnim()
    RequestAndWaitDict("misshair_shop@hair_dressers")
    local OldCoords = {x=0,y=0,z=0,h=0}
    OldCoords.x, OldCoords.y, OldCoords.z = table.unpack(GetEntityCoords(PlayerPedId()))
    OldCoords.h = GetEntityHeading(PlayerPedId())
    print("OldCoords.x, OldCoords.y, OldCoords.z, OldCoords.h", OldCoords.x, OldCoords.y, OldCoords.z, OldCoords.h)
    local closestchair = GetClosestHairChair()
    PosBase = closestchair.posI
    local ped = PlayerPedId()
    HairNetScene = NetworkCreateSynchronisedScene(PosBase, RotBase, 2, true, false, 1.0, 0.0, 1.0)
    NetworkAddPedToSynchronisedScene(ped, HairNetScene, "misshair_shop@hair_dressers", "keeper_hair_cut_a", 1000.0, -1000.0, 69, 0, 1000.0, 0)
    NetworkStartSynchronisedScene(HairNetScene)
    Wait(math.floor(11.93333 * 1000 * .66))
    NetworkStopSynchronisedScene(HairNetScene)
    SetEntityCoords(PlayerPedId(), OldCoords.x, OldCoords.y, OldCoords.z-0.85)
    SetEntityHeading(PlayerPedId(), OldCoords.h)
end